package com.operators;

public class ForLoop {
	
	public static void main(String args[]) {
		
		//creating for loop method
		
		/**
		 *    syntax :for( initialization;condition;increment/decrement){
		 *    }
		 * */
		
		for (int j = 3; j<=9; j++) {
			System.out.println("j value is :: "+j);
			
		}
	}

}
