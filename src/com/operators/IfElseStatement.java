package com.operators;

public class IfElseStatement {
	// main method
	public static void main(String args[]) {

		// variables declaration
		int a = 40;
		int b = 10;

		// if else statement with true condition
		if (a > b) {
			System.out.println("if else condition is true");
		} else {
			System.out.println("if else condition is true");
			System.out.println("hello");

		}
		// if else statement with false condition
		if (a < b) {
			System.out.println("if else condition is false");
		} else {
			System.out.println("if else codition is false");
			System.out.println("hello to");
		}

	}

}
