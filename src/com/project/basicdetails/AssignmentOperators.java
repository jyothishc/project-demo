package com.project.basicdetails;

public class AssignmentOperators {

	public static void main(String args[]) {
		// assignment operators +=, -=, *=, /*,%=
		int a = 8;

		float j = 8;
		System.out.println(a += 10); // 18
		System.out.println(a -= 10); // 8
		System.out.println(j %= 10); // 0.8
		System.out.println(j *= 10); // 8.0

	}

}
