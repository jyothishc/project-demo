package com.project.basicdetails;

public class RelationalOperators {

	public static void main(String args[]) {

		// RelationsOperators <, >, <=, >=,

		int i = 30;
		int j = 60;

		System.out.println(i < j);
		System.out.println(i > j);
		System.out.println(i <= j);
		System.out.println(i >= j);
		System.out.println(i != j);
		System.out.println(i == j);

	}

}
